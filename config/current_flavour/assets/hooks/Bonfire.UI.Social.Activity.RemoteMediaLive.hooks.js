let playerModule;
function loadPlayerModule(url) {
  return (playerModule ||= import(url));
}

export default {
  mounted() {
    this.initPlayer();
  },

  updated() {
    if (!this.player) this.initPlayer();
  },

  destroyed() {
    this.destroyPlayer();
  },

  async initPlayer() {
    const target = this.el.querySelector("[data-vidstack]");
    // Nothing to do for GIFs / iframes / images rendered without the player.
    if (!target || this.player || this.creating) return;

    const source = target.querySelector("source");
    const type = source?.type || "";
    const isHls = /mpegurl/i.test(type) || /\.m3u8(\?|$)/i.test(source?.src || "");
    const autoPlay = target.hasAttribute("autoplay");
    const viewType = target.tagName === "AUDIO" ? "audio" : "video";

    if (type && !isHls && document.createElement(viewType).canPlayType(type) === "") {
      this.showFallback();
      return;
    }

    const bundleUrl =
      target.dataset.playerBundle ||
      new URL("/assets/vidstack_player.js", window.location.origin).href;

    this.creating = true;
    try {
      const { VidstackPlayer, VidstackPlayerLayout, HLS } = await loadPlayerModule(bundleUrl);

      const player = await VidstackPlayer.create({
        target,
        viewType,
        autoPlay,
        muted: target.hasAttribute("muted"),
        // load strategy: lazy by default (load when scrolled into view), eager when
        // autoplaying, and defer to play for HLS so our local hls.js is set first.
        load: autoPlay ? "eager" : isHls ? "play" : "visible",
        layout: new VidstackPlayerLayout(),
      });

      // Use the locally-bundled hls.js instead of vidstack's default CDN loader.
      const useLocalHls = (provider) => {
        if (provider?.type === "hls") provider.library = HLS;
      };
      useLocalHls(player.provider);
      player.addEventListener("provider-change", (event) => useLocalHls(event.detail));

      // Surface load failures (CORS, unsupported codec, network) via the fallback.
      player.addEventListener("error", () => this.showFallback());

      if (viewType === "video") {
        player.addEventListener("loaded-metadata", () => {
          const v = player.querySelector("video");
          const w = v?.videoWidth || 0;
          const h = v?.videoHeight || 0;
          if (!w || !h) return;
          player.style.aspectRatio = `${w} / ${h}`;
          if (h > w) {
            player.style.width = "auto";
            player.style.maxHeight = "70vh";
            player.style.marginInline = "auto";
          }
        });
      }

      this.player = player;
    } catch (e) {
      console.error("vidstack: failed to initialise player", e);
      this.showFallback();
    } finally {
      this.creating = false;
    }
  },

  destroyPlayer() {
    if (this.player) {
      try {
        this.player.destroy();
      } catch (_) {}
      this.player = null;
    }
  },

  showFallback() {
    const fallback = this.el.querySelector('[data-role="media-fallback"]');
    const target = this.el.querySelector("[data-vidstack]");
    if (fallback) fallback.style.display = "block";
    if (target) target.style.display = "none";
  },
};
