// Hides the wrapping <li> when the widget renders nothing. Persistence
// lives in the Bonfire settings layer now, so there's no localStorage
// bridge here — this hook is purely visual.

export default {
  mounted() {
    this.sync();
  },

  updated() {
    this.sync();
  },

  sync() {
    const li = this.el.closest("li");
    if (!li) return;
    const hidden = this.el.dataset.state === "hidden";
    if (hidden) {
      li.style.display = "none";
    } else if (li.style.display === "none") {
      li.style.display = "";
    }
  },
};
